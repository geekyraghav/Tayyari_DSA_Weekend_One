package BasicsOfJava;

public class IncrementPlusPlus {
    public static void main(String[] args) {
        int x = 10;
        System.out.println(--x); // x = 9, 9
        System.out.println(x--); // 9, x = 8
        System.out.println(x); // 8

        int     y    =    ++x; System.out.println(x+y);
    }
}
