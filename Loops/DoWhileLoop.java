package Loops;

public class DoWhileLoop {
    public static void main(String[] args) {
        int i = 23;
        do{
            System.out.print(i+" ");
            i++;
        } while(i<=10);

        int k = 18;
        while(k<=10){
            System.out.print(k+" ");
            k++;
        }

        for(int j=19;j<=10;j++){
            System.out.print(j+" ");
        }
    }
}
