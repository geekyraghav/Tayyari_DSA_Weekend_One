package ArraysAndArrayLists;

public class SwapTwoNumbers {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(a+" "+b);

        int temp = a; // temp = 10
        a = b; // a = 20
        b = temp; // b = 10

//        a = a + b;
//        b = a - b;
//        a = a - b;

        System.out.println(a+" "+b);
    }
}
