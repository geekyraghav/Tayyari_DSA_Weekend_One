package Strings;

public class SubstringsOfString {
    public static void main(String[] args) {
        String s = "saksham"; // idx is from 0 to 6
        System.out.println(s.substring(3)); // 3 and aage ka sab
        System.out.println(s.substring(1,6)); // 1 to 5 print hua
        System.out.println(s.substring(2,2)); // kucch nahi hoga
    }
}
