package BitManipulation;

public class BitwiseOperators {
    public static void main(String[] args) {
        System.out.println(49&29);
        System.out.println(49|29);
        System.out.println(49^29);
        System.out.println(18^18);
        System.out.println(45^0);
        System.out.println(8>>2);
    }
}
